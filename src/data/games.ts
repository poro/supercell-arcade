export function getImplementedGames(): Game[] {
  return GAMES.filter(g => g.implemented);
}

export function getGamesByCategory(category: Game['category']): Game[] {
  return GAMES.filter(g => g.category === category);
}

export function getNeocortexGames(): Game[] {
  return GAMES.filter(g => g.usesNeocortex);
}

export function getGameById(id: string): Game | undefined {
  return GAMES.find(g => g.id === id);
}
